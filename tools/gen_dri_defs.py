import sys
from pathlib import Path
from datetime import datetime

collision_counter = 0

def enumerate_source_files(base_path, key):
  definitions = {}

  sources = list(Path(base_path).rglob("*.c"))
  for source in sources:
    print("Found source file:",source)

    for line in reversed(list(open(source))):
      if (key in line):
        clean = line.replace(' ', '').split('(')[1].split(')')[0].split(',')

        group = str(clean[0])
        name = str(clean[1])
        print("\tFound driver definition (GROUP: {0}, NAME: {1})".format(group, name))

        definitions[group] = "{0},{1}".format(definitions[group],name)

  return definitions


header_preamble = '''
/*\n
 * This file was generated by gen_dri_defs.py.\n
 * Date of Generation: {0}\n
*/\n
\n
#ifndef AUTOGEN_ARC_DRIVERS_DRI_DEFS\n
#define AUTOGEN_ARC_DRIVERS_DRI_DEFS\n
\n
#include \"drivers/resource.h\"\n
#include <stdint.h>\n
#include <stdint.h>\n
\n
''' .format(datetime.today().strftime("%d-%m-%Y"))

header_postamble = '''
#define ARC_DRIDEF_PCI_TERMINATOR ((uint32_t)-1)\n
#define ARC_DRIDEF_ACPI_TERMINATOR ((uint64_t)-1)\n
\n
int dridefs_int_func_empty();\n
size_t dridefs_size_t_func_empty();\n
void *dridefs_void_func_empty();\n
\n
#endif // AUTOGEN_ARC_DRIVERS_DRI_DEFS\n
'''

def construct_dri_defs_header(definitions, out_header_file, out_source_file):
  out_header = open(out_header_file, "w")

  out_header.write(header_preamble)

  # TODO: For each extension define a new table

  out_header.write(header_postamble)

  return 0

source_preamble = '''
/*\n
 * This file was generated by gen_dri_defs.py.\n
 * Date of Generation: {0}
*/\n
\n
#include \"drivers/dri_defs.h\"\n
\n
'''.format(datetime.today().strftime("%d-%m-%Y"))

source_postamble = '''
int dridefs_int_func_empty() {\n
\treturn -1;\n
}\n
\n
size_t dridefs_size_t_func_empty() {\n
\treturn 0;\n
}\n
\n
void *dridefs_void_func_empty() {\n
\treturn NULL;\n
}\n
\n
'''

def construct_dri_defs_source(definitions, out_header_file, out_source_file):
  out_source = open(out_source_file, "w")

  out_source.write(source_preamble)

  # TODO: Write the tables

  out_source.write(source_postamble)

  return 0

def main():
  if (len(sys.argv) < 5):
    printf("Usage: python gen_dri_defs.py DRIVER_DEFINITION_MACRO_NAME path/to/source/root path/to/output/header/file path/to/output/source/file")
    return -1

  definitions = enumerate_source_files(sys.argv[2], sys.argv[1])

  r = construct_dri_defs_header(definitions, sys.argv[3], sys.argv[4])
  r = r + construct_dri_defs_source(definitions, sys.argv[3], sys.argv[4])
  return r

if (__name__ == "__main__"):
    r = main()
    print("Cumulative Error:", )
    print("Collision Counter:", collision_counter)
    sys.exit(collision_counter + r)
